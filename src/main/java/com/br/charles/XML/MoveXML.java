package com.br.charles.XML;

public class MoveXML {

	private String id;

	private String player;

	private PositionXML position;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getPlayer() {
		return player;
	}

	public void setPlayer(String player) {
		this.player = player;
	}

	public PositionXML getPosition() {
		return position;
	}

	public void setPosition(PositionXML position) {
		this.position = position;
	}
}